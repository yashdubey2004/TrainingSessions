package day2.Demo2.someCodes;

public class printTables {
    public static void main(String[] args) {
        for(int i = 2; i <= 6; i++){
            for(int j = 1; j <= 10; j++){
                System.out.println(i + " X " + j + " = " + i * j + " ");
            }
            System.out.println();
        }
    }
}
